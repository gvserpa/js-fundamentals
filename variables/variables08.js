/*

Create two numeric variables and display in the console the results of the following operations:

Addition

Subtraction

Multiplication

Division

Remainder of the division (modulus)

*/


const num1 = 5;
const num2 = 15;


// Add 

const resultAdd = num1 + num2
console.log('Add:', resultAdd)

// Subtractions

const resultSubtraction = num1 - num2
console.log('Subtraction:', resultSubtraction)

// Multiplication

const resultMultiplication = num1 * num2
console.log('Multiplication:', resultMultiplication)

// Division

const resultDivision = num1 / num2 
console.log('Division:', resultDivision)

// Remainder (modulus)

const resultModulus = num1 % num2
console.log('Modulus:', resultModulus)
